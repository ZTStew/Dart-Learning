void main() {
  print("test");

  // Strings
  var test = "test1";
  // String test = "test2";
  print(test);

  // Integers
  // var num = 3;
  int num = 3;

  print(num);

  // Dynamic
  // String word = "test3";
  dynamic dyn = "test3";
  dyn = 4;

  print(dyn);

  // Constants && Finals
  // const -> defined at compile time
  // final -> defined at run time
  const String con = "here";
  final String fin = "there";

  print(con);
  print(fin);
}
